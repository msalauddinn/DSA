package OneDimensionalArrays.Arrays;

public class ArrayOutput {
    public static void main(String[] args) {
        int[] arr = {8, 3, 5, 7, 25, -3}; // index -> 0 to arr.length-1

        for (int i = 0; i < arr.length; i++){
            System.out.print(arr[i] + " ");
        }
        System.out.println();
    }
}
