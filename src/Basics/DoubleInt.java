package Basics;

public class DoubleInt {
    public static void main(String[] args) {
        System.out.println(5 / 2); // int / int
        System.out.println(5.0 / 2.0); // double / double
        System.out.println(5.0 / 2); // double / int
        System.out.println(5 / 2.0); // int / double
    }
}
