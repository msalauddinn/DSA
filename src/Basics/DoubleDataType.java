package Basics;

public class DoubleDataType {
    public static void main(String[] args) {
        // int x = 3.1415; // ERROR
        double a = 3.1415;
        System.out.println(a);

        double x = 5;
        double y = 2;
        System.out.println(x + y);
        System.out.println(x - y);
        System.out.println(x * y);
        System.out.println(x / y);
    }
}
